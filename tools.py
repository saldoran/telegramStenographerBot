async def show_stats():
    """–ü–æ–∫–∞–∑–∞—Ç—å —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫—É –±–∞–∑—ã –¥–∞–Ω–Ω—ã—Ö"""
    db_manager = DatabaseManager()
    await db_manager.init_database()
    
    print("üìä –°—Ç–∞—Ç–∏—Å—Ç–∏–∫–∞ Telegram Stenographer Bot")
    print("=" * 50)
    
    # –°—Ç–∞—Ç–∏—Å—Ç–∏–∫–∞ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–π
    users = await db_manager.get_tracked_users()
    print(f"üë• –û—Ç—Å–ª–µ–∂–∏–≤–∞–µ–º—ã—Ö –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–π: {len(users)}")
    
    if users:
        print("\nüìã –°–ø–∏—Å–æ–∫ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–π:")
        for i, user in enumerate(users, 1):
            user_info = f"{i}. ID: {user['user_id']}"
            if user['username']:
                user_info += f" (@{user['username']})"
            if user['first_name']:
                user_info += f" - {user['first_name']}"
                if user['last_name']:
                    user_info += f" {user['last_name']}"
            print(f"   {user_info}")
    
    await db_manager.close()


